package ru.itvitality.otus.ba.after;

public class K {
    private int i;
    private int j;
    private int h;

    public int v() {
        return i * j * i;
    }

    public int getJ() {
        return j;
    }

    public void setJ( int j ) {
        this.j = j;
    }

    public int getH() {
        return h;
    }

    public void setH( int h ) {
        this.h = h;
    }

    public int getI() {
        return i;
    }

    public void setI( int i ) {
        this.i = i;
    }
}
